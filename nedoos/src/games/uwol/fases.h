void fases_init(void)
{
    u16 i,j,tmp;
    const u8 *ptr;
 
    ptr=fases_data;
 
    for(i=0;i<45;i++)
    {
        fases[i].descriptor=*ptr++;
        for(j=0;j<10;j++)
        {
            tmp=*ptr++;
            tmp=(*ptr++<<8)|tmp;
            fases[i].obj[j]=tmp;
        }
        for(j=0;j<3;j++)
        {
            tmp=*ptr++;
            tmp=(*ptr++<<8)|tmp;
            fases[i].movil[j]=tmp;
        }
        for(j=0;j<10;j++) fases[i].coin[j]=*ptr++;
    }
}
